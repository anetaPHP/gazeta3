    /**
     * FindAllCategoryById.
     *
     * @return mixed
     */
    public function findAllCategoryById()
    {
        /*
         * @return Category[]
         */
        return $this->createQueryBuilder('k')
            ->andWhere('k.id IS NOT NULL')
            ->orderBy('k.id', 'ASC')
            ->getQuery()
            ->getResult();
    }

    /**
